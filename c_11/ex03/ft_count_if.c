int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int i;
	int j;

	if (length == 0)
		return (0);
	while (tab[i])
	{
		if ((*f)(tab[i]) != 0)
			j++;
		i++;
	}
	return (j);
}
