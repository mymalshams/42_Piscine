int		ft_any(char **tab, int (*f)(char*))
{
	int		i;
	int		results;

	results = 0;
	i = 0;
	while (tab[i])
	{
		if ((*f)(tab[i] != 0))
		{
			results = 1;
			break ;
		}
		i++;
	}
	return (results);
}
